int				static_error(int x)
{
	static int	ret = 0;
	static int	aff = 1;

	if (x == 1)
		ret = -1;
	if (x == 2)
		ret = 0;
	if (x == 3)
		aff = 0;
	if (x == 4)
		return (aff);
	if (x == 5)
		aff = 1;
	return (ret);
}
