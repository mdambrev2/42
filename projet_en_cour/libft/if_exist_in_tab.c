#include "libft.h"

int		if_exist_in_tab(char **tab, char *str)
{
	int x;

	x = 0;
	while(tab[x] != 0)
	{
		if(ft_strcmp(tab[x], str) == 0)
			return(1);
		x++;
	}
	return(0);
}
